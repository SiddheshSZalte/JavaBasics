package javabasics;

public class Switch_Case {
	public static void main( String args[]) {
		int number = 7;
		switch(number) {
		case 1 : System.out.println("food");
		break;
		case 2 : System.out.println("beverages");
		break;
		case 3 : System.out.println("samosa");
		break;
		default : System.out.println("give a valid entry");
		}
	}
}

