package javabasics;

public class AssignmentOperator {
	public static void main(String args[]) {
		int a = 10;
		a *=50;
		//a = a+10 => a+=10
		//a = a-10 => a-=10
		//a = a*10 => a*=10
		//a = a\10 => a\=10
		
		System.out.println(a);
	    System.out.println(a-=10);
	    System.out.println(a+=10);
	    System.out.println(a*=10);
	    System.out.println(a/=10);
		
	}

}
 